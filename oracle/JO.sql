create table LESBATIMENTS
(
  NOMBAT VARCHAR2(20) not null
    constraint B_PK
    primary key,
  NUMERO NUMBER(4)
    constraint B_CK1
    check (numero > 0),
  RUE VARCHAR2(40),
  VILLE VARCHAR2(20)
);

create table LESLOGEMENTS
(
  NLOGEMENT NUMBER(3) not null
    constraint L_CK1
    check (nLogement > 0),
  CAPACITE NUMBER(3)
    constraint L_CK2
    check (capacite > 0),
  NOMBAT VARCHAR2(20) not null
    constraint L_FK
    references LESBATIMENTS,
  constraint L_PK
  primary key (NLOGEMENT, NOMBAT)
);

create table LESSPORTIFS
(
  NSPORTIF NUMBER(4) not null
    constraint SP_PK
    primary key
    constraint SP_CK1
    check (nSportif > 0),
  NOMS VARCHAR2(20),
  PRENOMS VARCHAR2(20),
  PAYS VARCHAR2(20),
  CATEGORIE VARCHAR2(10)
    constraint SP_CK2
    check (categorie in ('feminin','masculin')),
  DATENAIS DATE,
  constraint SP_UN
  unique (NOMS, PRENOMS)
);

create table LESEQUIPES
(
  NEQUIPE NUMBER(4) not null
    constraint EQ_CK1
    check (nEquipe > 0),
  NSPORTIF NUMBER(4) not null
    constraint EQ_FK
    references LESSPORTIFS,
  constraint EQ_PK
  primary key (NEQUIPE, NSPORTIF)
);

create table LESLOCATAIRES
(
  NSPORTIF NUMBER(4) not null
    constraint LS_PK
    primary key
    constraint LS_FK1
    references LESSPORTIFS,
  NLOGEMENT NUMBER(4),
  NOMBAT VARCHAR2(20),
  constraint LS_FK2
  foreign key (NLOGEMENT, NOMBAT) references LESLOGEMENTS
);

create table LESDISCIPLINES
(
  DISCIPLINE VARCHAR2(25) not null
    constraint DI_PK
    primary key
);

create table LESEPREUVES
(
  NEPREUVE NUMBER(3) not null
    constraint EP_PK
    primary key
    constraint EP_CK3
    check (nEpreuve > 0),
  NOME VARCHAR2(20),
  FORME VARCHAR2(13)
    constraint EP_CK1
    check (forme in ('individuelle','par equipe','par couple')),
  DISCIPLINE VARCHAR2(25)
    constraint EP_FK
    references LESDISCIPLINES,
  CATEGORIE VARCHAR2(10)
    constraint EP_CK2
    check (categorie in ('feminin','masculin','mixte')),
  NBS NUMBER(2)
    constraint EP_CK4
    check (nbs > 0),
  DATEEPREUVE DATE,
  PRIX NUMBER(4,2)
    constraint EP_CK5
    check (prix >= 0)
);

create table LESINSCRIPTIONS
(
  NINSCRIT NUMBER(4) not null
    constraint I_CK1
    check (nInscrit > 0),
  NEPREUVE NUMBER(4) not null
    constraint I_FK
    references LESEPREUVES,
  constraint I_PK
  primary key (NINSCRIT, NEPREUVE)
);

create table LESRESULTATS
(
  NEPREUVE NUMBER(4) not null
    constraint RES_PK
    primary key,
  GOLD NUMBER(4),
  SILVER NUMBER(4),
  BRONZE NUMBER(4),
  constraint RES_FK1
  foreign key (GOLD, NEPREUVE) references LESINSCRIPTIONS,
  constraint RES_FK2
  foreign key (SILVER, NEPREUVE) references LESINSCRIPTIONS,
  constraint RES_FK3
  foreign key (BRONZE, NEPREUVE) references LESINSCRIPTIONS,
  constraint RES_CK1
  check (gold<>silver and silver<>bronze and gold<>bronze)
);

create table LESDOSSIERS_BASE
(
  NDOSSIER NUMBER not null
    constraint D_PK
    primary key,
  NUTIL NUMBER,
  DATEEMISSION DATE
);

create table LESBILLETS
(
  NBILLET NUMBER not null,
  NDOSSIER NUMBER not null
    constraint BI_FK1
    references LESDOSSIERS_BASE,
  NEPREUVE NUMBER(3)
    constraint BI_FK2
    references LESEPREUVES,
  constraint BI_PK
  primary key (NBILLET, NDOSSIER)
);

